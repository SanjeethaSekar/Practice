package HelloWorld;

public class IncrementAndDecrement {

	public static void main(String[] args) {
		
		// post increment 
		int a = 1;
		int b = a++;
		
		System.out.println(a);
		System.out.println(b);
		
		// pre increment
		int c = 1;
		int d = ++c;
		
		System.out.println(c);
		System.out.println(d);
		
		
	}

}
